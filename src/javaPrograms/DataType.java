package javaPrograms;

public class DataType {
	static int j;  // integer
	static double dd; // double
	/*
	 * Primitive data types: integer, double, char, boolean {String 
	 */

	public static void main(String[] args) {
		int i = 10; 
		double k = 100.23;
		char c = 'a'; // character
		System.out.println("Value of integer=" + i);
		System.out.println("Value of double=" + k);
		System.out.println("Value of char=" + c);
		System.out.println("Default value  of double=" + dd);
		System.out.println("default Value of integer=" + j);
      
		boolean b1=true;  // boolean
		System.out.println("Boolean value="+b1);
		
		String s1="Selenium";
		System.out.println("String value="+s1);
		
	}

}
